#include <stdio.h>
int main()
{
	/*字符串:一个或多个字符的序列
	双引号仅用来告知编译器，括起来的是字符串
	字符串是以字符数组来存储的，字符串的最后一个元素一定是\0(空字符：ASCIL码为0的字符，不是NULL) 
	*/
	//定义字符串的几种方式
	char names[]={'j','a','c','k'};//这是字符数组 
	char names1[50]={'j','a','c','k','\0'};//这是字符串，字符串和字符数组的区别是最后一个元素一定是'\0' 
	char names2[]="jack";//默认有一个字符是空字符
	char *names3="jack";
	//声明存储字符串的数组时，数组大小至少比所存储的字符度多一  
	printf("请输入新名称：");
	scanf("%s",names1);
	printf("%s",names1);
}
