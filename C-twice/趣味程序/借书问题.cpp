#include <stdio.h>
/*
小明有5本书，要借给A,B,C三位小朋友，若没人每次只能借一本
则有多少种不同的借法
*/
/*
穷举，假设三个人借其中一本当三个人借的书不一样时打印结果 
*/ 
int main()
{
	int a, b, c, count = 0;
	printf("结果是：\n");
	for(a = 1; a <= 5; a++)
	{
		for(b = 1; b <= 5; b++)
		{
			for(c = 1; b != a && c <= 5; c++)//只有在a != b的前提下才会进入这个循环考虑c的值 
			{
				if(c != a && c != b) //只有在三个值各不相同的情况下才进行打印 
				{
					printf(count % 5 ? "%2d:%d,%d,%d\t" : "%2d,%d,%d,%d\n",++count,a,b,c);
				}
			}
		}
	}
}
