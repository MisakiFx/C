#include <stdio.h>
int main()
{
	//顺序查找法：利用循环从数组里一个一个依次查找，找到对应的数则跳出循环
	//选择排序 :在一个数组里从第一个数字开始，利用循环寻找之后数字比他更小的数字，
	//找到后进行交换；直到进行外循环到倒数第二个数字为止 
	int a[10] = {7,8,9,55,44,12,45,65,32,84}, num, i;
	printf("请输入要查找的数：");
	scanf("%d",&num);
	for(i = 0; i < 10; i++)
	{
		if(a[i] == num)
		{
			printf("找到了！下标为：%d，查找次数为：%d",i,i+1);
			break;
		}
	}
	if(i >= 10)
	{
		printf("没找到！");
	}
}
